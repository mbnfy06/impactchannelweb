"use client";
import {
    useScroll,
    useTransform,
    motion,
} from "framer-motion";
import React, { useEffect, useRef, useState } from "react";

interface TimelineEntry {
    title: string;
    content: React.ReactNode;
}

const TimelineItem = ({
    item,
    index,
    total,
    scrollYProgress,
}: {
    item: TimelineEntry;
    index: number;
    total: number;
    scrollYProgress: any;
}) => {
    // Calculate activation threshold based on index
    // Adjust these values to sync with the moving line
    const step = 1 / total;
    const start = step * index;
    const end = start + step * 0.5;

    const color = useTransform(
        scrollYProgress,
        [start, end],
        ["#737373", "#eab308"] // neutral-500 -> yellow-500
    );

    return (
        <div className="flex justify-start pt-10 md:pt-40 md:gap-10">
            <div className="sticky flex flex-col md:flex-row z-40 items-center top-40 self-start max-w-xs lg:max-w-sm md:w-full">
                <div className="h-10 absolute left-3 md:left-3 w-10 rounded-full bg-neutral-900 flex items-center justify-center border border-white/10">
                    <div className="h-4 w-4 rounded-full bg-neutral-800 border border-neutral-700 p-2" />
                </div>
                <motion.h3
                    style={{ color }}
                    className="hidden md:block text-xl md:pl-20 md:text-5xl font-bold"
                >
                    {item.title}
                </motion.h3>
            </div>

            <div className="relative pl-20 pr-4 md:pl-4 w-full">
                <motion.h3
                    style={{ color }}
                    className="md:hidden block text-2xl mb-4 text-left font-bold"
                >
                    {item.title}
                </motion.h3>
                {item.content}{" "}
            </div>
        </div>
    );
};

export const Timeline = ({ data }: { data: TimelineEntry[] }) => {
    const ref = useRef<HTMLDivElement>(null);
    const containerRef = useRef<HTMLDivElement>(null);
    const [height, setHeight] = useState(0);

    useEffect(() => {
        if (ref.current) {
            const rect = ref.current.getBoundingClientRect();
            setHeight(rect.height);
        }
    }, [ref]);

    const { scrollYProgress } = useScroll({
        target: containerRef,
        offset: ["start 10%", "end 50%"],
    });

    const heightTransform = useTransform(scrollYProgress, [0, 1], [0, height]);
    const opacityTransform = useTransform(scrollYProgress, [0, 0.1], [0, 1]);

    return (
        <div
            className="w-full bg-transparent font-sans md:px-10"
            ref={containerRef}
        >
            <div className="max-w-7xl mx-auto py-20 px-4 md:px-8 lg:px-10">
                <h2 className="text-lg md:text-4xl mb-4 text-white max-w-4xl font-bold">
                    Nuestro Proceso de Trabajo
                </h2>
                <p className="text-neutral-300 text-sm md:text-base max-w-sm">
                    Un enfoque metodológico probado. Desde el análisis inicial hasta la entrega final, cuidamos cada detalle para garantizar el éxito.
                </p>
            </div>

            <div ref={ref} className="relative max-w-7xl mx-auto pb-20">
                {data.map((item, index) => (
                    <TimelineItem
                        key={index}
                        item={item}
                        index={index}
                        total={data.length}
                        scrollYProgress={scrollYProgress}
                    />
                ))}
                <div
                    style={{
                        height: height + "px",
                    }}
                    className="absolute md:left-8 left-8 top-0 overflow-hidden w-[2px] bg-[linear-gradient(to_bottom,var(--tw-gradient-stops))] from-transparent from-[0%] via-neutral-700 to-transparent to-[99%]  [mask-image:linear-gradient(to_bottom,transparent_0%,black_10%,black_90%,transparent_100%)] "
                >
                    <motion.div
                        style={{
                            height: heightTransform,
                            opacity: opacityTransform,
                        }}
                        className="absolute inset-x-0 top-0  w-[2px] bg-gradient-to-t from-yellow-500 via-orange-500 to-transparent from-[0%] via-[10%] rounded-full"
                    />
                </div>
            </div>
        </div>
    );
};
